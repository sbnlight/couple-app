/** 按「今天」日期串取当天题目(与 questions.ts 同一套稳定题号逻辑) */
export function quizForDate(dayStr: string): { id: number; quiz: Quiz } {
  const days = Math.floor(Date.parse(`${dayStr}T00:00:00Z`) / 86_400_000)
  const id = ((days % QUIZZES.length) + QUIZZES.length) % QUIZZES.length
  return { id, quiz: QUIZZES[id] }
}
